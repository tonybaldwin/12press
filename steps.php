<li>We were entirely ready to have God remove all these defects of character.</li>
<li>We humbly asked Him to remove our shortcomings.  </li>
<li>We made a list of all persons we had harmed, and became willing to make amends to them all.</li>
<li>We made direct amends to such people wherever possible, except when to do so would injure them or others.</li>
<li>We continued to take personal inventory and when we were wrong promptly admitted it.</li>
<li>We sought through prayer and meditation to improve our conscious contact with God as we understood Him, praying only for knowledge of His will for us and the power to carry that out.</li>
<li>Having had a spiritual awakening as a result of these steps, we tried to carry this message to addicts, and to practice these principles in all our affairs.</li>
