<h1>The Twelves Steps of Narcotics Anonymous</h1>
<ol>
<li>We admitted that we were powerless over our addiction, that our lives had become unmanageable.</li>
<li>We came to believe that a Power greater than ourselves could restore us to sanity.</li>
<li>We made a decision to turn our will and our lives over to the care of God as we understood Him.</li>
<li>We made a searching and fearless moral inventory of ourselves.  </li>
